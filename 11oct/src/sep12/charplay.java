package sep12;

public class charplay {

	public static void main(String[] args) {
		char ch = 'a';
		System.out.println(ch);
		int i = ch;
		System.out.println(i);
		
		i = ch + 5;
		System.out.println(i);
		
		ch = (char)(ch + 1);
		System.out.println(ch);
		
		ch++;
		System.out.println(ch);
	}

}
